/**
 * Beispielprogramm zur Demonstration der Verarbeitung von Oktalzahlen
 * 
 * @author Michael Inden
 * 
 * Copyright 2012 by Michael Inden 
 */
public class OctalHexadecimalExample 
{
	public static void main(final String[] args) 
	{
		final int octalLiteral = 0567;
		final int hexaLiteral = 0xABC;
		
		System.out.println("octalLiteral " + octalLiteral);
		System.out.println("hexaLiteral " + hexaLiteral);
	}
}